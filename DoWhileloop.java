package ControlStatement;

public class DoWhileloop {
	public static void main(String[] args) {
		int i = 0;
		System.out.println("First 5 even number \n");
		do {
			System.out.println(i); // statement
			i= i+2 ; // increment
		}while(i<=10); // condition
		}
	}


